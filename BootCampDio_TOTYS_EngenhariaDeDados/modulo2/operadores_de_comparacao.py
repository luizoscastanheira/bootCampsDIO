# Operadores de comparação - usados para comparar dois valores retornadno SEMPRE um booleano True ou False
# igualdade e diferença
saldo = 450
saque = 200
print(saldo == saque) # teste SE é IGUAL
print(saldo != saque) # teste SE é DIFERENTE

# maior que, maior ou igual, menor que, menor ou igual
print(saldo > saque)
print(saldo >= saque)

print(saldo < saque)
print(saldo <= saque)
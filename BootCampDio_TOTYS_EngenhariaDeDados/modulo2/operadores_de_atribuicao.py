# Operadores de atribuição - permitem atribuir um valor inicial a uma variável / constante ou sobrescrver o valro de uma variável
# é representado pelo sinal de igualdade: = ou =+ ou -=
# Atribuição simples
saldo = 500
print(saldo)

saldo += 200
print(saldo)

saldo = 500
saldo -= 100
print(saldo)

saldo = 500
saldo *= 2
print(saldo)

saldo = 500
saldo /= 5
print(saldo)

saldo = 500
saldo //= 5
print(saldo)

saldo = 500
saldo %= 3
print(saldo)

saldo = 500
saldo **= 2
print(saldo)